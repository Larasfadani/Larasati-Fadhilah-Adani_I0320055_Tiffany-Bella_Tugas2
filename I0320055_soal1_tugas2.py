#Menghitung konversi suhu celcius ke fahrenheit
print("Mengonversi Suhu dari Celcius ke Fahrenheit")
celcius = float(input("Suhu dalam celcius = "))
fahrenheit = ((9/5) * celcius) + 32
print("Suhu adalah", celcius,"celcius")
print("Suhu dalam fahrenheit adalah = ", fahrenheit, "fahrenheit")

#Menghitung konversi suhu reamur ke kelvin
print("Mengonversi Suhu dari Reamur ke Kelvin")
reamur = (4/5) * celcius
print("Suhu dalam reamur = ", reamur)
